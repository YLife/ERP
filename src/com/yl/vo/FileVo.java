package com.yl.vo;
/**
 * 封装文件条件信息
 * @author Mr.Yang
 *
 */
public class FileVo {
	private String fileName;

	public FileVo() {
		super();
	}

	public FileVo(String fileName) {
		super();
		this.fileName = fileName;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

}
